
package variablesconverting;

public class VariablesConverting {

    public static void main(String[] args) {
        
        
        
        String doubleString = "12";
        
        double myNumber = Double.parseDouble(doubleString);
        
        int intNum = Integer.parseInt(doubleString);
       
        System.out.println(myNumber);
        System.out.println(intNum);
        
    }
    
}
